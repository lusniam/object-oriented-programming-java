    private void ChangeNum(int num){
        if (num==0&&TextLabel.getText().equals("0")){
            return;
        }
        else if(TextLabel.getText().equals("0")){
            TextLabel.setText(Integer.toString(num));
        }
        else if(isNewNumber){
            TextLabel.setText(Integer.toString(num));
            isNewNumber=false;
        }
        else{
            TextLabel.setText(TextLabel.getText()+Integer.toString(num));
        }
    }
    
    private void HandleFunc(char zn){
        if(znak!='0'&&isNewNumber==false){
            this.ButtonEqual.doClick();
        }
        number=Double.parseDouble(TextLabel.getText());
        znak=zn;
        isNewNumber=true;
    }

