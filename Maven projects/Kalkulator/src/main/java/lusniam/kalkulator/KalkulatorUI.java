    private void ButtonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEqualActionPerformed
        double result;
        switch(znak){
            case '+':{
                result = number + Double.parseDouble(TextLabel.getText());
                break;
            }
            case '-':{
                result = number - Double.parseDouble(TextLabel.getText());
                break;
            }
            case '*':{
                result = number * Double.parseDouble(TextLabel.getText());
                break;
            }
            case '/':{
                result = number / Double.parseDouble(TextLabel.getText());
                break;
            }
            case '^':{
                result = Math.pow(number,Double.parseDouble(TextLabel.getText()));
                break;
            }
            default:
                return;
        }
        znak='0';
        TextLabel.setText(result%1.0!=0 ? String.format("%s", result) : String.format("%.0f", result));
    }//GEN-LAST:event_ButtonEqualActionPerformed

